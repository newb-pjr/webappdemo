<template>
	<div class="music-list" ref="musicList">
    <div class="back" @click="back">
      <i class="icon-back"></i>
    </div>
    <h1 class="title" v-html="title"></h1>
    <div class="bg-image" :style="bgStyle" ref="bgImg">
      <div class="play-wrapper">
        <div class="play" ref="playBtn" v-show="songs.length>0" @click="randomPlay">
          <i class="icon-play"></i>
          <span class="text">随机播放全部</span>
        </div>
      </div>
      <div class="filter" ref="filter"></div>
    </div>
    <div class="bg-layer" ref="layer"></div>
    <scroll class="list" :data="songs" ref="list" :probe-type="probeType" :listen-scroll="listenScroll" @scroll="scroll">
    	<div class="song-list-wrapper">
    		<song-list :songs="songs" @select="select" :rank="rank"></song-list>
    	</div>
    	<div class="loading-container" v-show="!songs.length">
    		<loading></loading>
    	</div>
    </scroll>
  </div>
</template>
<script type="text/ecmascript-6">
	import Scroll from 'base/scroll/scroll'
	import SongList from 'base/song-list/song-list'
	import Loading from 'base/loading/loading'
	import { prefixStyle } from 'common/js/dom'
	import { mapActions } from 'vuex'
	import { playListMixin } from 'common/js/mixin'

	const transform = prefixStyle('transform')
	const filter = prefixStyle('filter')

	export default {
		mixins: [playListMixin],
		props: {
			title: {
				type: String,
				default: ''
			},
			bgImg: {
				type: String,
				default: ''
			},
			songs: {
				type: Array,
				default: []
			},
			rank: {
				type: Boolean,
				default: false
			}
		},
		data () {
			return {
				scrollY: -1
			}
		},
		watch: {
			scrollY (newY) {
				let TranslateY = Math.max(this.minTranslateY, newY)
				let zIndex = 0
				let scale = 1
				this.$refs.layer.style[transform] = `translate3d(0,${TranslateY}px,0)`
				const percent = Math.abs(newY / this.imageHeigt)
				if (newY > 0) {
					scale = 1 + percent
					zIndex = 10
				} else {
					let blur = Math.min(percent * 20, 20)
					this.$refs.bgImg.style[filter] = `blur(${blur}px)`
				}
				this.$refs.bgImg.style[transform] = `scale(${scale})`
				if (newY < this.minTranslateY) {
					this.$refs.bgImg.style.paddingTop = 0
					this.$refs.bgImg.style.height = '40px'
					this.$refs.bgImg.style[filter] = 'blur(0px)'
					this.$refs.playBtn.style.display = 'none'
					zIndex = 10
				} else {
					this.$refs.bgImg.style.paddingTop = '70%'
					this.$refs.bgImg.style.height = '0'
					this.$refs.playBtn.style.display = ''
				}
				this.$refs.bgImg.style.zIndex = zIndex
			}
		},
		computed: {
			bgStyle () {
				return `background-image:url(${this.bgImg})`
			}
		},
		created () {
			this.probeType = 3
			this.listenScroll = true
		},
		mounted () {
			this.imageHeigt = this.$refs.bgImg.clientHeight
			this.minTranslateY = -this.imageHeigt + 40
			this.$refs.list.$el.style.top = `${this.imageHeigt}px`
		},
		methods: {
			handlePlayList (playList) {
				const bottom = playList.length > 0 ? '60px' : ''
				this.$refs.list.$el.style.bottom = bottom
				this.$refs.list.refresh()
			},
			scroll (pos) {
				this.scrollY = pos.y
			},
			back () {
				this.$router.back()
			},
			select (item, index) {
				this.selectSong({
					list: this.songs,
					index
				})
			},
			randomPlay () {
				console.log(333)
				this.randomSong({
					list: this.songs
				})
			},
			...mapActions([
				'selectSong',
				'randomSong'
				])
		},
		components: {
			Scroll,
			SongList,
			Loading
		}
	}
</script>
